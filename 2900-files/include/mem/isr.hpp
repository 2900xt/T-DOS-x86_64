extern "C" void ISR_HANDLER(Registers* regs){

 if (g_ISRHandlers[regs->interrupt] != NULL)
        g_ISRHandlers[regs->interrupt](regs);

    else if (regs->interrupt >= 32)
        cout("Unhandled interrupt %d!\n", regs->interrupt);

    else 
    {
        cout("Unhandled exception %d\n", regs->interrupt);
        cout(g_Exceptions[regs->interrupt]);
        cout("\n");
        cout("CODE: %d ",regs->interrupt);
        cout("CS %d ",regs->cs);
        cout("DS %d ",regs->ds);
        cout("EAX %d ",regs->eax);
        cout("EBP %d ",regs->ebp);
        cout("EBX %d\n",regs->ebx);
        cout("ECX %d ",regs->ecx);
        cout("EDI %d ",regs->edi);
        cout("EDX %d ",regs->edx);
        cout("EFLAGS %d ",regs->eflags);
        cout("EIP %d\n",regs->eip);
        cout("ERROR: %d ",regs->error);
        cout("ESP %d ",regs->esp);
        cout("KERN_ESP %d ",regs->kern_esp);
        cout("SS %d\n\n",regs->ss);
    }
    return;
}
